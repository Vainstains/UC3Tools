    public static CoasterWorldSaveFileMsg CreateBlankWorld()
    {
        var assembly = Assembly.GetExecutingAssembly();
        using var stream = assembly.GetManifestResourceStream("LibUC3.blank.uc3world");
        
        if (stream == null)
        {
            throw new FileNotFoundException($"Resource blank.uc3world not found.");
        }

        using var memoryStream = new MemoryStream();
        
        stream.CopyTo(memoryStream);
        return Message.Parse<CoasterWorldSaveFileMsg>(memoryStream.ToArray());
    }
    
